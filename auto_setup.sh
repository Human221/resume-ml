#!/bin/bash

# Автоматическая установка через SSH (требует пароль)
# Использование: ./auto_setup.sh

SERVER_IP="176.109.111.108"
SERVER_USER="root"
SCRIPT_NAME="setup_server.sh"

echo "=========================================="
echo "АВТОМАТИЧЕСКАЯ УСТАНОВКА НА СЕРВЕРЕ"
echo "=========================================="
echo ""
echo "Сервер: $SERVER_USER@$SERVER_IP"
echo ""
echo "⚠️  ВНИМАНИЕ: Потребуется ввести пароль от сервера"
echo ""

# Копирование скрипта на сервер
echo "Копирование скрипта установки на сервер..."
scp setup_server.sh $SERVER_USER@$SERVER_IP:~/

# Выполнение скрипта на сервере
echo ""
echo "Запуск установки на сервере..."
echo "Введите пароль от сервера:"
ssh $SERVER_USER@$SERVER_IP "chmod +x ~/setup_server.sh && ~/setup_server.sh"

echo ""
echo "=========================================="
echo "Установка завершена!"
echo "=========================================="

